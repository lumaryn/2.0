public class Sweet {

        private String name;
        private int weight;
        private int price;
        private String option;

        public Sweet(String name, int weight, int price, String option) {

            this.name = name;
            this.weight = weight;
            this.price = price;
            this.option = option;
        }

        public String getName() {
            return name;
        }

        public void setName(String name) {
            this.name = name;
        }

        public int getWeight() {
            return weight;
        }

        public void setWeight(int weight) {
            this.weight = weight;
        }

        public int getPrice() {
            return price;
        }

        public void setPrice(int price) {
            this.price = price;
        }

        public String getOption() {
            return option;
        }

        public void setOption(String option) {
            this.option = option;
        }
    }



